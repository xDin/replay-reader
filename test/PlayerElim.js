const assert = require('assert');
const Replay = require('../src/Classes/Replay');
const event = require('../src/event');

function parse(bytes, EngineNetworkVersion, Major, Minor) {
  const replay = new Replay(Buffer.from(bytes));
  replay.header = { EngineNetworkVersion, Major, Minor };
  replay.info = { IsEncrypted: false };

  // event.goTo() expects an offset to pop after parsing when not encrypted
  replay.addOffsetByte(1, bytes.length);

  return event(replay, { startPos: 0, length: bytes.length, group: 'playerElim' });
}

